  <div class="appeal-point">
    <h2 id="section1" class="about_free_title">フリーランスについて</h2>
  </div>

  <div class="about__text">
  <p>フリーランスとは、社会的に独立した形で、自分が得意なスキルを活かして提供し、報酬を得るという新しい働き方をする人のことです。<span class="about_free_red">会社員のように企業や団体などと雇用契約を結ばず、個人に任された案件ごとに契約を結び、作業を進める</span>ことになります。</p>
  <p>このようなフリーランスの働き方は、システムエンジニアやプログラマーなどのエンジニア職や、デザイナー、イラストレーター、ライターなどのクリエイター職など幅広い職種があります。</p>
  </div>
  <div class="appeal-point">
    <h2 id="section2" class="about_free_title">フリーランスITエンジニアのメリット</h2>
  </div>
  <div class="about__text">
  <p>フリーランスのITエンジニアは自宅で仕事をするか、お客様のオフィスへと常駐するという2つの働き方に分けることができます。</p>
  <p>まずは、そんなフリーランスITエンジニアのメリットを見ていきましょう。</p>
  </div>
  <div class="appeal-point">
    <h3 id="title1" class="about_free_lead">働く時間や場所が自由に選べる</h3>
  </div>
  <div class="about__text">
  <p>在宅フリーランスITエンジニアの場合は、納期さえしっかり守れば基本的にはいつどこで仕事をしても良いのです。カフェや図書館で仕事をしたり、昼間に家事をして夕方から仕事を始める…なんて生活も可能です。</p>
  <p>また、インターネット環境さえ整っていれば仕事ができるため、都会から離れて田舎でのんびり仕事をすることもできます。</p>
  <p>このように、<span class="about_free_red">在宅ITエンジニアは時間や場所の制約から解放された働き方が可能</span>です。</p>
  </div>
  <div class="appeal-point">
    <h3 id="title2" class="about_free_lead">スキルアップにつながる</h3>
  <div class="about__text">
  <p>特定の会社で正社員として働いていると、思うようにスキルアップできない場合があります。会社にとっては、社員が同じ仕事をしてくれたほうが助かるという点が多いです。</p>
  <p>しかし、技術的に上昇志向がある方からすると良いことばかりではありませんよね。また、同じ仕事ばかりしていると、面白みがなくなったりスキルが偏ってしまう場合があります。</p>
  <p>フリーランスITエンジニアはある程度の経験を持っていれば自由に仕事を選ぶことができるため、スキルパスに合った案件を選んで、スキルアップを目指すことができます。</p>
  </div>
  <div class="appeal-point">
    <h3 id="title3" class="about_free_lead">請け負う案件を選べる</h3>
  <div class="about__text">
  <p>フリーランスITエンジニアは基本的に自分で案件を選ぶことができます。知人やフリーランス仲間から案件を紹介してもらったり、エージェントから案件を紹介してもらうことも可能です。</p>
  <p>どのような方法でも、自分の気が進まない案件は避けることができますし、<span class="about_free_red">自分でやりたい仕事を選べるというメリット</sapn>があります。</p>
  </div>
  <div class="appeal-point">
    <h3 id="title4" class="about_free_lead">年収がアップする可能性がある</h3>
  <div class="about__text">
  <p>フリーランスITエンジニアは会社を介さない分、基本的に売り上げはすべて自分のものになります。
    <br />エージェント経由であっても、会社員をしているより年収がアップする可能性があるのです。
  </p>
  </div>
  <div class="appeal-point">
    <h3 id="title5"  class="about_free_lead">フリーランスと会社員の違い</h3>
  <div class="about__text">
  <p>フリーランスと会社員、派遣社員の大きな違いは、契約の結び方にあります。派遣社員は所属する派遣会社と、会社員は勤務先の企業と雇用契約を結びます。</p>
  <p>これに対して、フリーランスは案件を個人で引き受けるのであって、発注元のクライアントに雇用されるわけではないのです。そのため、フリーランスがクライアントと結ぶ契約は、<span class="about_free_red">雇用契約ではなく業務委託契約を結ぶこと</span>になります。</p>
  </div>
  <div class="appeal-point">
    <h3 id="title6"  class="about_free_lead">業務委託について</h3>
  <div class="about__text">
  <p>フリーランスがクライアントと<span class="about_free_red">直接契約をして、案件を受けることを業務委託</span>と言います。フリーランスとクライアントの関係は、ビジネス上では常に対等なのです。</p>
  <p>フリーランスとクライアントの契約交渉では、具体的な作業内容や価格、契約期間などについて話し合いを行い、<span class="about_free_red">双方が合意してはじめて業務委託契約</span>が結ばれます。
    <br />業務委託契約には請負契約と（準）委任契約の2つがあり、どちらの契約を結ぶのかは案件ごとに異なります。
  </p>
  </div>
  <div class="appeal-point">
    <h3 id="title7"  class="about_free_lead">請負契約と（準）委任契約の違いについて</h3>
  <div class="about__text">
  <p>請負契約は、仕事を最後まで完成させるまでが契約の基本です。それと同時に瑕疵担保責任が生まれるので、成果物に何らかの欠陥が見つかった場合は、損害賠償や補修に応じる必要があります。</p>
  <p>また、完璧な成果を保証できない場合は、委任契約が結ばれます。委任契約は、仕事の完成義務や瑕疵担保責任がありません。しかし、専門家などに期待される範囲で業務を行う義務（善管注意義務）を怠ってしまうと、損害賠償責任に発展する可能性があります。</p>
  </div>
  <div class="appeal-point">
    <h3 id="title8"  class="about_free_lead">見積書について</h3>
  <div class="about__text">
  <p>フリーランスに転向したての方にとって、特に悩ましいのが見積書の存在です。まだ相場観がつかめないうちは、案件に対して高いか安いかの判断が難しいでしょう。しかし、自分が納得できる価格かどうかが大切です。</p>
  <p>ITエンジニアの場合は、システム構築やデータベース作成、コーディングなどに項目を分けて考えることで、自分の納得のいく金額を計算しやすいでしょう。</p>
  <p>また、納品後のメンテナンスや更新作業などの項目もクライアントにあらかじめ伝えておくことで、いろいろなトラブルを防ぐことができるのです。こういった意味では、日頃からクライアントとお金の話がしっかりできるように、信頼関係を築き上げておくことが大切です。</p>
  </div>
  <h2 id="section3"  class="about_free_title">フリーランスと個人事業主の違い</h2>
  <div class="about__text">
  <p>日本の事業は独立開業時の形態によって、法人事業か個人事業のいずれかに該当します。法人事業の主体は、〇〇会社や〇〇法人と呼ばれるような団体や組織になりますが、個人事業はあくあまでも一個人が事業主となります。</p>
  <p>フリーランスと個人事業は一見似ているように思えますが、微妙に異なるので注意しましょう。</p>
  <p>フリーランスは1つの案件ごとに契約を結ぶ働き方となるので、個人事業主のことをフリーランスとは言い難いでしょう。<br />そして、法人を設立したうえでフリーランスとして働くという場合もあるので、「フリーランス＝個人事業主」とは言い切れません。
  </p>
  </div>
  <div class="about__text">
  <div class="appeal-point">
    <h3 id="title9" class="about_free_lead">フリーランスITエンジニアとしての働き方</h3>
  <p>フリーランスITエンジニアは作業場所の違いによって、<span class="about_free_red">常駐型と在宅型の2つのワークスタイルが選べます。</span></p>
  <p>常駐型はクライアントになる企業に出向し、現場で作業を行うので、一般的な会社員と似たようなワークスタイルになります。そして、在宅型はクライアントから案件を持ち帰って、自分の事務所や自宅、シェアオフィスなどで作業を進めていきます。</p>
  <p>では、常駐型と在宅型にはそれぞれどのような特徴があるのでしょうか。常駐型と在宅型のメリットとデメリットをご紹介します。</p>
  </div>
  <div class="appeal-point"><h3 id="title10" class="about_free_lead">常駐型のメリットとデメリット</h3>
  <div class="about__text">
  <p>常駐型のフリーランスの案件は、クライアントの企業に常駐し、現場と連携しながら作業を進めていくというスタイルが基本になります。在宅型と比べて常駐型は、開発環境や危機管理などの理由によって、需要が多い傾向があります。</p>
  <p>クライアントに提供される開発環境を使用するため、自分で準備をする必要がなく、クライアントと近くにいるので、案件の摺り合わせが容易にでき、効率よく作業が進められます。</p>
  <p>また、<span class="about_free_red">常駐型は安定して案件を獲得でき、毎月決まった支払いがあるという大きなメリット</span>もあります。そして、常駐型は現場に出向しなければ出会えないたくさんの同業者と作業をすることが可能です。
    <br />周りから刺激を受けたり人脈を広げたり、情報交換をしたりと、常駐型のワークスタイルで得ることも多いでしょう。
  </p>
  <p>しかし、常駐型は現場と連携し協調しながら案件を進めていくので、人によっては窮屈に感じることもあります。</p>
  </div>
  <div class="appeal-point"><h3 id="title11" class="about_free_lead">在宅型のメリットとデメリット</h3>
  <div class="about__text">
  <p>在宅型のフリーランスの大きなメリットは、自分の裁量で作業場所や作業時間を決めることができるという点です。この自由度の高さによって、仕事と家事、育児などの両立ができるので、主婦（主夫）層にも注目を集めているのです。</p>
  <p>また、在宅型のフリーランスITエンジニアは契約に違反をしない限り、自分で開発したソースコードを資産として蓄積しやすいというメリットもあります。</p>
  <p>しかし、在宅型は自由に作業スタイルが選べる反面、作業量が安定しないこともあり、計画的に作業を進めていかなければならないという徹底的な自己管理能力が求められます。</p>
  <p>自分で開発環境を準備するため、コスト・時間を有する場合があり、クライアントと離れての作業になるため、認識のズレなどによる出戻りが発生する可能性もあります。</p>
  </div>
  <h2 id="section4"  class="about_free_title">フリーランスITエンジニアになるためには？</h2>
  <div class="about__text">
  <p>では、フリーランスITエンジニアになるには何からはじめれば良いのでしょうか。ここからは、フリーランスITエンジニアになるために行うべき手続きなどをご紹介していきます。</p>
  </div>
  <div class="appeal-point"><h3 id="title12" class="about_free_lead">年金と医療保険の切り替え</h3>
  <div class="about__text">
  <p>会社員からフリーランスITエンジニアになるには、はじめに年金と公的医療保険を切り替える手続きをしなくてはなりません。</p>
  <p>公的医療保険は国民健康保険に切り替えるのが基本なのですが、退職する前の会社で入っていた健康保険をそのまま任意継続することも可能です。</p>
  <p>しかし、この任意継続をするには次の条件があります。</p>
  <p>・被保険者の期間が会社の退職日までに継続して2ヵ月以上あり、かつ退職日から20日以内に申請した場合のみ、継続して2年間の適用を受けることができるのです。</p>
  <p>また、これらのほかに国民健康保険の健康保険に加入したり、家族の健康保険の被扶養者となる方法もあります。
    <br />（被扶養者になる場合は、収入の上限があります）
  </p>
  <p>フリーランスの年金制度については、個人事業主として独立する場合と法人を設立する場合によって違いがあります。個人事業主は国民年金に加入しますが、法人の場合は厚生年金に強制加入となります。</p>
  </div>
  <div class="appeal-point"><h3 id="title13" class="about_free_lead">開業届を提出する</h3>
  <div class="about__text">
  <p class="about_free">個人事業主として活動を始めるにあたり、開業届出書の提出が必要になります。開業届の用紙は国税庁のホームページからダウンロードするか、税務署の窓口でもらうこともできます。
    <br /><a href="https://www.nta.go.jp/taxes/tetsuzuki/shinsei/annai/shinkoku/annai/04.htm" target=”_blank”>国税局HP</a>
  </p>
  <p>開業届には次のような項目があるので正確に記入しましょう。</p>
  </div>
  <p>&#8226;納税地
    <br />&#8226;書類の提出日
    <br />&#8226;所轄の税務署名
    <br />&#8226;氏名
    <br />&#8226;電話番号
    <br />&#8226;マイナンバー
    <br />&#8226;屋号
    <br />&#8226;職業
    <br />&#8226;開業した場所の住所
    <br />&#8226;所得の種類
    <br />&#8226;具体的な事業内容
    <br />&#8226;開業日
  </p>
  <div class="about__text">
  <p>開業届出書は、自分用の控えとして記入済みの書類をもう一部コピーしておきましょう。
    <br />税務署に提出する際に開業届をコピーと合わせて提出し、印を押してもらった控えを手元に保管しておきましょう。
    <br />これは、小規模企業救済制度の申し込みで開業届の控えが必要になる場合があるからです。
  </p>
  </div>
  <div class="appeal-point"><h3 id="title14" class="about_free_lead">青色申告承認申請書の提出</h3>
  <div class="about__text">
  <p>会社員や派遣社員の場合は所属する会社が原則として年末調整をしていますが、個人事業主になる場合は、自分で確定申告をしなければなりません。
    <br />その際、考えておきたいのは、確定申告を白色申告でするか青色申告でするかです。
  </p>
  <p><span class="about_free_red">青色申告だと65万円の特別控除を受けることができたり、損失を3年間繰り越したりできるのですが、簿記の知識が無いと難しかったり、複式簿記の帳簿を作成しなければならないというハードルもあります。</span></p>
  <p>白色申告の場合は金銭的なメリットはないのですが、複式簿記の帳簿をつける必要が無いので、その手軽さから白色申告を選ぶ方も少なくありません。しかし、平成26年分から白色申告も記帳が必要になったので、それ以前と比べると手軽さは薄れたと言えます。</p>
  <p>青色申告を選ぶ際は、開業届出書を税務署に提出する際にまとめて手続きを行うと良いですね。</p>
  </div>
  <h2 id="section5" class="about_free_title">フリーランスITエンジニアに向いている人とは？</h2>
  <div class="about__text">
  <p>最後に、どのような人がフリーランスITエンジニアに向いているのかを見ていきましょう。ぜひ、ご自身の性格やタイプと比較してみてくださいね。</p>
  </div>
  <div class="appeal-point"><h3 id="title15" class="about_free_lead">ある程度の技術スキルがある人</h3>
  <div class="about__text">
  <p>クライアントの企業は労働力を外部から調達するので、フリーランスに、当たり前に仕事ができることを期待します。わざわざ実力不足の人を呼んで育成するなんてもってのほかです。</p>
  <p>そのため、フリーランスITエンジニアはある程度の技術力があることが大切です。</p>
  </div>
